public class 最大子序列和 {

    /**
     * 给定一个整数数组 nums ，找到一个具有最大和的连续子数组（子数组最少包含一个元素），返回其最大和。
     * <p>
     *  
     * <p>
     * 示例 1：
     * <p>
     * 输入：nums = [-2,1,-3,4,-1,2,1,-5,4]
     * 输出：6
     * 解释：连续子数组 [4,-1,2,1] 的和最大，为 6 。
     */
    public int maxSubArray(int[] nums) {

        int maxNum = nums[0];
        int pre = 0;

        for (int num : nums) {

            pre = Math.max(maxNum, pre + num);
            maxNum = Math.max(maxNum, pre);
        }
        return maxNum;
    }
}
